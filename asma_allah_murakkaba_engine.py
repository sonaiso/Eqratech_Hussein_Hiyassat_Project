import pandas as pd
from base_reconstruction_engine import BaseReconstructionEngine
from reconstruction_utils import reconstruct_from_base_df


class AsmaAllahMurakkabaEngine(BaseReconstructionEngine):
    SHEET_NAME = 'أسماء الله المركبة'

    @classmethod
    def make_df(cls):
        names = ["غافر الذنب","قابل التوب","رفيع الدرجات","أحسن الخالقين","خالق كل شيء","أسرع الحاسبين","سريع الحساب","سريع العقاب","شديد العقاب","شديد المحال","فالق الحب والنوى","فالق الإصباح","القائم بالقسط","أهل التقوى","أهل المغفرة","أرحم الراحمين","أحكم الحاكمين","فعال لما يريد","ذو الفضل","ذو الرحمة","ذو المغفرة","ذو القوة","ذو العرش","ذو انتقام","ذو الطول","ذو المعارج","خير الناصرين","خير الرازقين","خير الفاصلين","خير الحاكمين","خير الفاتحين","خير الغافرين","خير الوارثين","خير الراحمين","خير المنزلين"]
        data = []
        for name in names:
            # نترك حقل الفونيمات فارغاً حتى لا يُعاد تركيب الاسم بدون المسافات
            data.append({
                "الأداة": name,
                "القالب/التركيب": "مركب إضافي أو وصفي",
                "الفونيمات": "",  # intentionally blank to preserve original spaced form
                "الحركات": "",
                "شرط/سياق": "يستخدم كاسم إلهي مركب",
                "الوظيفة النحوية": "اسم علم إلهي مركب",
                "الوظيفة الدلالية": "دلالة على الذات الإلهية أو صفاتها",
                "الوظيفة الصرفية": "مركب غالبًا",
                "الوظيفة الصوتية": "اسم صوتي واضح",
                "الوظيفة الاشتقاقية": "مركب غالبًا",
                "ملاحظات": "اسم مركب من أسماء الله الحسنى"
            })
        dataframe = pd.DataFrame(data)
        return reconstruct_from_base_df(dataframe)
